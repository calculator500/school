#include <unistd.h>

char	*ft_strrev(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	while (str[i] >= 0)
	{
		write(1, &str[i], 1);
		i--;
	}
	return (str);
}
